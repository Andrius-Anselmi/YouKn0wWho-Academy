#include <iostream>
using namespace std;

// 219158/problem/C  C. Simple Calculator

int main() {

  long long x, y;

  cin >> x >> y;

  cout << x << " + " << y << " = " << x + y << endl;
  cout << x << " * " << y << " = " << x * y << endl;
  cout << x << " - " << y << " = " << x - y << endl;

  return 0;

}
